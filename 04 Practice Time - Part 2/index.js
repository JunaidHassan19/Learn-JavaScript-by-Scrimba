let passwordLength = 13;

function generateRandomPassword() {
  let password = "";
  for (i = 0; i < passwordLength; i++) {
    const randomIndex = Math.floor(Math.random() * characters.length);
    password += characters[randomIndex];
  }
  return password;
}

